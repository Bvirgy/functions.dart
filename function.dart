// Demonstrating Data Types in Dart

void main() {
  // int data type
  int age = 25;
  print("Age: $age");

  // double data type
  double height = 1.75;
  print("Height: $height");

  // String data type
  String name = "John Doe";
  print("Name: $name");

  // List data type
  List<String> fruits = ["apple", "banana", "orange"];
  print("Fruits: $fruits");

  // Map data type
  Map<String, int> scores = {
    "Alice": 95,
    "Bob": 80,
    "Charlie": 75,
  };
  print("Scores: $scores");
}
